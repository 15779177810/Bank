	/**
	 * 存钱按钮监听
	 * @param evt
	 */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
    	try {
    	double money =Double.parseDouble(jTextField1.getText());
    	Transfer drawTransfer = null;
    	transferService = new TransferServiceImpl(user);
		drawTransfer = transferService.deposit(user.getUsername(), money);
		tableModel.addRow(drawTransfer.toDataArray());
		jTextField1.setText("");
		} catch (TransferException e) {
			JOptionPane.showMessageDialog(null, e.getMessage());
		}catch(NumberFormatException e){
			JOptionPane.showMessageDialog(null, "输入有误");
		}
    }//GEN-LAST:event_jButton1ActionPerformed

