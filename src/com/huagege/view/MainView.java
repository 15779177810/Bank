    /**
     * 转账按钮监听
     * @param evt
     */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
    	try {
    		if(jTextField2.getText().equals(""))
    			throw new TransferException("对方账号为空");
    		if(user.getUsername().equals(jTextField2.getText()))
    			throw new TransferException("不能向自己转账");
    	double money =Double.parseDouble(jTextField1.getText());
    	String target = jTextField2.getText();
    	Transfer drawTransfer = null;
    	transferService = new TransferServiceImpl(user);
		drawTransfer = transferService.transfer(user.getUsername(), target, money);
		tableModel.addRow(drawTransfer.toDataArray());
		jTextField1.setText("");
		jTextField2.setText("");
		} catch (TransferException e) {
			JOptionPane.showMessageDialog(null, e.getMessage());
		}catch(NumberFormatException e){
			JOptionPane.showMessageDialog(null, "请正确输入");
		}catch( ArrayIndexOutOfBoundsException e){
			JOptionPane.showMessageDialog(null, "交易失败");
		}
    }

